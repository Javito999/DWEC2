class Contador {

    constructor(numero) {
        this.numero = numero;
    }

    getNumero() {
        return this.numero;
    }

  
    setNumero(valor) {
        this.numero = valor;
    }
    //m√©todos
    incrementa() {
        this.numero++;
    }

    decrementa() {
        this.numero--;
    }
};


